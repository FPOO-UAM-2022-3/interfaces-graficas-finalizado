    public void eventoCierre() {
        System.out.println("Guardando");
        try {
            ObjectOutputStream salida = new ObjectOutputStream(new FileOutputStream("taller.obj"));
            salida.writeObject(this.miTaller);
            salida.close();
        } catch (Exception ex) {
            System.out.println("" + ex);
        }
        System.exit(0);
    }

    public void cargarSesion() {
        try {
            ObjectInputStream entrada = new ObjectInputStream(new FileInputStream("taller.obj"));
            this.miTaller = (Taller) entrada.readObject();
        } catch (Exception ex) {
            System.out.println("" + ex);
        }
    }

    public void guardarArchivo(String contenido, String extension) {
        try {
            JFileChooser file = new JFileChooser();
            file.showSaveDialog(this);
            //String ruta = "" + file.getCurrentDirectory();
            try (BufferedWriter save = new BufferedWriter(new FileWriter(file.getSelectedFile().toString() + "." + extension))) {
                save.write(contenido);
            }
            JOptionPane.showMessageDialog(null, "El archivo se a guardado Exitosamente",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Su archivo no se ha guardado",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
        }
    }

    public void actualizarTablaTecnicos() {
        String nombresColumnas[] = {"Cedula", "Nombre", "Telefono", "Años Experiencia", "Especializacion"};
        DefaultTableModel miModelo = new DefaultTableModel(null, nombresColumnas);
        this.tblTecnicos.setModel(miModelo);
        for (Tecnico actual : this.miTaller.getMisTecnicos()) {
            String fila[] = new String[nombresColumnas.length];
            fila[0] = actual.getCedula();
            fila[1] = actual.getNombre();
            fila[2] = actual.getTelefono();
            fila[3] = "" + actual.getAñosExperiencia();
            fila[4] = actual.getEspecializacion();
            miModelo.addRow(fila);
        }
    }

    public void actualizarTablaRelacionTecnicosCubiculos(String cedula) {
        String nombresColumnas[] = {"Id", "Nombre"};
        DefaultTableModel miModelo = new DefaultTableModel(null, nombresColumnas);
        this.tblRelacionTecnicoCubiculo.setModel(miModelo);
        Tecnico encotrado = this.miTaller.buscarTecnico(cedula);
        if (encotrado != null) {
            for (Cubiculo cubiculoActual : encotrado.getMisCubiculos()) {
                String fila[] = new String[nombresColumnas.length];
                fila[0] = cubiculoActual.getId();
                fila[1] = cubiculoActual.getNombre();
                miModelo.addRow(fila);
            }
        }

    }

    public void actualizarComboCubiculos() {
        this.cbCubiculosTecnico.removeAllItems();
        for (Cubiculo actual : this.miTaller.getMisCubiculos()) {
            cbCubiculosTecnico.addItem(actual.getNombre());
        }
    }

