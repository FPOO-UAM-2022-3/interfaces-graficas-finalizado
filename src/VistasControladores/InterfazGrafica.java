    private void btnAgregarRelacionCubiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRelacionCubiculoActionPerformed
        // TODO add your handling code here:
        String cedula = this.txtCedulaPersona.getText();

        Tecnico encontrado = this.miTaller.buscarTecnico(cedula);
        if (encontrado != null) {
            int indice = this.cbCubiculosTecnico.getSelectedIndex();
            Cubiculo seleccionado = this.miTaller.getMisCubiculos().get(indice);
            encontrado.getMisCubiculos().add(seleccionado);
            JOptionPane.showMessageDialog(this, "Asociado");
        }
        actualizarTablaRelacionTecnicosCubiculos(cedula);
    }//GEN-LAST:event_btnAgregarRelacionCubiculoActionPerformed

